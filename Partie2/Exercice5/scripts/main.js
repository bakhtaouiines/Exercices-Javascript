let day = 5
switch (day) {
    case 1:
        day = "Lundi"
        console.log("Lundi")
        break
    case 2:
        day = "Mardi"
        console.log("Mardi")
        break
    case 3:
        day = "Mercredi"
        console.log("Mercredi")
        break
    case 4:
        day = "Jeudi"
        console.log("Jeudi")
        break
    case 5:
        day = "Vendredi"
        console.log("Vendredi")
        break
    case 6:
        day = "Samedi"
        console.log("Samedi")
        break
    case 7:
        day = "Dimanche"
        console.log("Dimanche")
        break
    default:
        day = "numéro de jour non valide"
        console.log("numéro de jour non valide")
        break

}